void set_baudrate_low(uint16_t baudrate_low)
{
    uint16_t low = (uint16_t)(baudrate & 0xFFFF);
    if (low != baudrate_low) {
        baudrate             = (baudrate & 0xFFFF0000) | (uint32_t)baudrate_low;
        baudrate_update_flag = true;
    }
#ifdef USE_UART_DEBUG
    printf("baudrate_low = %d\n", baudrate_low);
#endif
}

void set_baudrate_high(uint16_t baudrate_high)
{
    uint16_t high = (uint16_t)((baudrate >> 16) & 0xFFFF);
    if (high != baudrate_high) {
        baudrate             = (baudrate & 0x0000FFFF) | ((uint32_t)baudrate_high << 16);
        baudrate_update_flag = true;
    }
#ifdef USE_UART_DEBUG
    printf("baudrate_high = %d\n", baudrate_high);
#endif
}

void baudrate_update(void)
{
    if (baudrate_update_flag) {
        baudrate_update_flag = false;
#ifdef USE_UART_DEBUG
        printf("baudrate = %d\n", baudrate);
#endif
        HAL_Delay(10);
        __disable_irq();
        eMBClose();
        eMBDisable();
        HAL_UART_MspInit(&huart1);
        MX_UART_Init(baudrate);
        eMBInit(MB_RTU, 0x01, 1, baudrate, MB_PAR_NONE);
        eMBEnable();
        __enable_irq();
    }
}
